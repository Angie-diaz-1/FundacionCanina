    public boolean validarCamposPerro() {
        String nombre = txtNombreCanino.getText();
        String identificador = txtIdentificador.getText();
        String edadAux = txtEdad.getText();
        String raza = txtRaza.getText();
        int comida = cbComida.getSelectedIndex();
        int estado = cbEstado.getSelectedIndex();
        String tratamiento = txtTratamiento.getText();

        if (nombre.isBlank() || identificador.isBlank() || edadAux.isEmpty() || raza.isEmpty() || comida == 0 || estado == 0 || tratamiento.isBlank()) {
            return true;
        } else {
            int edad = Integer.parseInt(edadAux);
            return false;
        }

    }

    private boolean validarCamposVaciosBuscarYEliminarPerro() {
        String identificador = txtIdentificador.getText();

        if (identificador.isBlank()) {
            return true;
        } else {
            return false;
        }

    }

    public void limpiarCamposPerro() {
        txtNombreCanino.setText("");
        txtIdentificador.setText("");
        txtEdad.setText("");
        txtRaza.setText("");
        cbComida.setSelectedItem("Seleccione");
        cbEstado.setSelectedItem("Seleccione");
        txtTratamiento.setText("");

    }

