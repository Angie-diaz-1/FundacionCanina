    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (validarCamposPerro()) {
            JOptionPane.showMessageDialog(null, "recuerda digitar todos los datos para agregar al perro  ");

        } else {

            String nombre = txtNombreCanino.getText();
            String identificador = txtIdentificador.getText();
            int edad = Integer.parseInt(txtEdad.getText());
            String raza = txtRaza.getText();
            String comida = String.valueOf(cbComida.getSelectedItem());
            String estado = String.valueOf(cbEstado.getSelectedIndex());
            String tratamiento = txtTratamiento.getText();
            
           System.out.print(this.fila);

            Perro perro = new Perro(nombre, identificador, edad, raza, comida, estado, tratamiento, this.fila);
            try {
                boolean respuesta = ventanaCuarto.getControladorCuarto().registrarPerro(perro);
                if (respuesta) {
                    JOptionPane.showMessageDialog(null, "El canino fue agregado correctamente al cuarto");
                    limpiarCamposPerro();
                    mostrarInfoenTabla();
                    ventanaCuarto.validate();
                    //ventanaCuarto.setVisible(true);

                    //this.dispose();
                }

            } catch (DocumentoDuplicadoException docuementoDuplicadoException) {
                JOptionPane.showMessageDialog(null, docuementoDuplicadoException.getMessage());

            }

        }


    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:

        if (validarCamposVaciosBuscarYEliminarPerro()) {
            JOptionPane.showMessageDialog(null, "Por favor recuerde digitar el numero de documento para buscar el canino");
        } else {
            String identificador = txtIdentificador.getText();
            Perro auxiliar = ventanaCuarto.getControladorCuarto().buscarPerro(identificador);
            if (auxiliar != null) {

                txtNombreCanino.setText(auxiliar.getNombre());
                txtIdentificador.setText(auxiliar.getIdentificador());
                txtEdad.setText(String.valueOf(auxiliar.getEdad()));
                txtRaza.setText(auxiliar.getRaza());
                String comida = String.valueOf(cbComida.getSelectedItem());
                String estado = String.valueOf(cbEstado.getSelectedItem());
                txtTratamiento.setText(auxiliar.getTratamiento());
                btnEditar.setEnabled(true);
                btnEliminar.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, "El canino bajo este documento --->  " + identificador + "  NO existe ");
                txtIdentificador.setText("");
                limpiarCamposPerro();
            }

    }//GEN-LAST:event_btnBuscarActionPerformed

    }
    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (validarCamposPerro()) {
            JOptionPane.showMessageDialog(null, "por favor recuerde COMPLETAR TODOS los datos del Canino al editar ");
        } else {

            String nombre = txtNombreCanino.getText();
            String identificador = txtIdentificador.getText();
            int Edad = Integer.parseInt(txtEdad.getText());
            String raza = txtRaza.getText();
            String comida = String.valueOf(cbComida.getSelectedItem());
            String estado = String.valueOf(cbEstado.getSelectedItem());
            String tratamiento = txtTratamiento.getText();

            Perro auxiliarPerro = new Perro(nombre, identificador, Edad, raza, comida, estado, tratamiento, this.fila);
            try {
                ventanaCuarto.getControladorCuarto().editarPerro(auxiliarPerro);
                JOptionPane.showMessageDialog(null, " Se edito CORRECTAMENTE el canino con Identificacion " + identificador);
                limpiarCamposPerro();
                mostrarInfoenTabla();

            } catch (documentoNoExisteExeption documentoNoExisteExeption1) {
                JOptionPane.showMessageDialog(null, documentoNoExisteExeption1.getMessage());
      
            }

        }


    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (validarCamposVaciosBuscarYEliminarPerro()) {
            JOptionPane.showMessageDialog(null, " Recuerda ingresar el numero de identificador del canino ");
        } else {

            String identificador = txtIdentificador.getText();
            boolean respuesta = ventanaCuarto.getControladorCuarto().eliminarPerro(identificador);
            if (respuesta) {
                JOptionPane.showMessageDialog(null, " se elimino CORRECTAMENTE el canino bajo esta identificacion " + identificador);
                limpiarCamposPerro();
                mostrarInfoenTabla();
            } else {
                JOptionPane.showMessageDialog(null, " NO se elimino el canino con el identificador " + identificador);
            }

        }


    }//GEN-LAST:event_btnEliminarActionPerformed

